static std::string ConvertAnsiToUtf8(const std::string& ansiString)
{
	int utf8Length = MultiByteToWideChar(CP_ACP, 0, ansiString.c_str(), -1, NULL, 0);
	std::wstring unicodeString(utf8Length, L'\0');
	MultiByteToWideChar(CP_ACP, 0, ansiString.c_str(), -1, &unicodeString[0], utf8Length);

	int utf8BufferSize = WideCharToMultiByte(CP_UTF8, 0, unicodeString.c_str(), -1, NULL, 0, NULL, NULL);
	std::string utf8String(utf8BufferSize, '\0');
	WideCharToMultiByte(CP_UTF8, 0, unicodeString.c_str(), -1, &utf8String[0], utf8BufferSize, NULL, NULL);

	return utf8String;
}

